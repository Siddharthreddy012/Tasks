#indexing
word="Thats one small step for a man, a giant leap for mankind"

# printing first occurrence of letter 'o' in forward 
print(word[6])

# printing fourth occurrence of letter 'a' in forward 
print(word[28])

# printing letter 'g' in backward
print(word[-22])

#printing second occurrence of letter 's' in backward
print(word[-46])

#slicing
word="Thats one small step for a man, a giant leap for mankind"

#printing one in forward
print(word[6:9:1])

#printing leap in forward
print(word[40:44:1])

#printing nam in backword
print(word[-27:-30:-1])

#printing llams in backward
print(word[-42:-47:-1])

#reversing the word using slicing 
